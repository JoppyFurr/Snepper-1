/*
 * Output in Digital format.
 */
int output_digital ()
{
    FILE *output = NULL;

    /* Silly snep wrote the bits backwards */
    for (int i = 0; i < (1 << 8); i++)
    {
        data[i] = (data[i] & 0x0001 ? 0x8000 : 0x0000) |
                  (data[i] & 0x0002 ? 0x4000 : 0x0000) |
                  (data[i] & 0x0004 ? 0x2000 : 0x0000) |
                  (data[i] & 0x0008 ? 0x1000 : 0x0000) |
                  (data[i] & 0x0010 ? 0x0800 : 0x0000) |
                  (data[i] & 0x0020 ? 0x0400 : 0x0000) |
                  (data[i] & 0x0040 ? 0x0200 : 0x0000) |
                  (data[i] & 0x0080 ? 0x0100 : 0x0000) |
                  (data[i] & 0x0100 ? 0x0080 : 0x0000) |
                  (data[i] & 0x0200 ? 0x0040 : 0x0000) |
                  (data[i] & 0x0400 ? 0x0020 : 0x0000) |
                  (data[i] & 0x0800 ? 0x0010 : 0x0000) |
                  (data[i] & 0x1000 ? 0x0008 : 0x0000) |
                  (data[i] & 0x2000 ? 0x0004 : 0x0000) |
                  (data[i] & 0x4000 ? 0x0002 : 0x0000) |
                  (data[i] & 0x8000 ? 0x0001 : 0x0000);
    }

    output = fopen ("16seg.hex", "w");
    if (output == NULL)
    {
        fprintf (stderr, "Unable to open 16seg.hex for output.\n");
        return -1;
    }

    fprintf (output, "v2.0 raw\n");

    for (int i = 0; i < (1 << 8); i++)
    {
        fprintf (output, "%x\n", data [i]);
    }

    fclose (output);
}

int main (void)
{
    int rc = EXIT_SUCCESS;

    /* Write the data to a file for the simulator */
    if (output_digital ())
    {
        rc = EXIT_FAILURE;
    }

    return rc;
}
